app.get("/stats", async (req, res) => {
  try {
    const { docteur } = req.query;
    
    if (docteur) {
      // Statistiques pour un docteur sp√©cifique
      if (!['dr-husni-said-habibi', 'dr-helios-blasco', 'dr-jean-eric-panacciulli'].includes(docteur)) {
        return res.status(400).json({ 
          success: false,
          message: "Docteur non valide" 
        });
      }
      
      const stats = await Ticket.aggregate([
        { $match: { docteur: docteur } },
        {
          $group: {
            _id: "$status",
            count: { $sum: 1 }
          }
        }
      ]);
      
      // Formatage des statistiques
      const formattedStats = {
        docteur: docteur,
        en_attente: 0,
        en_consultation: 0,
        termine: 0,
        desiste: 0,
        total: 0
      };
      
      stats.forEach(stat => {
        formattedStats[stat._id] = stat.count;
        formattedStats.total += stat.count;
      });
      
      res.json(formattedStats);
    } else {
      // Statistiques globales par docteur
      const statsByDoctor = await Ticket.aggregate([
        {
          $group: {
            _id: {
              docteur: "$docteur",
              status: "$status"
            },
            count: { $sum: 1 }
          }
        },
        {
          $group: {
            _id: "$_id.docteur",
            stats: {
              $push: {
                status: "$_id.status",
                count: "$count"
              }
            },
            total: { $sum: "$count" }
          }
        }
      ]);
      
      res.json(statsByDoctor);
    }
  } catch (error) {
    console.error("Erreur lors de la r√©cup√©ration des statistiques:", error);
    res.status(500).json({ message: "Erreur de r√©cup√©ration des statistiques" });
  }
});

